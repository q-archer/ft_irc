#include "libft.h"

void		ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	index;

	index = 0;
	if (s && f)
	{
		while (s[index] != '\0')
		{
			(*f)(index, &s[index]);
			index = index + 1;
		}
	}
}
