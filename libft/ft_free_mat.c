#include "libft.h"

void		ft_free_mat(char ***mat)
{
	int		i;

	i = -1;
	if (*mat != NULL)
	{
		while ((*mat)[++i] != NULL)
			ft_strdel(&(*mat)[i]);
		free(*mat);
	}
	*mat = NULL;
}
